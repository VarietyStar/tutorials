package helloWorld;

//Hello World Tutorial
//Class: Permissions Statement Name
public class Hello {
    public static void main(String[] args) {
        System.out.println("This will be printed");
        System.out.print("This will also be printed");
    }
}

/*
public means that anyone can access it.
static means that you can run this method without 
creating an instance of Main.
void means that this method doesn't return any value.
main is the name of the method.

public static void main(String[] args) is the entry point of the code
*/